char *editorPrompt(char *prompt, void (*callback)(char *, int), char first_char) {
  size_t bufsize = 128;
  char *buf = malloc(bufsize);

  size_t buflen = 1;
  buf[0] = first_char;
  buf[1] = '\0';

  while (1) {
    editorSetStatusMessage(prompt, buf);
    editorRefreshScreen();

    int c = editorReadKey();
    if (c == DEL_KEY || c == CTRL_KEY('h') || c == BACKSPACE) {
      if (buflen != 0) buf[--buflen] = '\0';
    } else if (c == '\x1b') {
      editorSetStatusMessage("");
      if (callback) callback(buf, c);
      free(buf);
      return NULL;
    } else if (c == '\r') {
      if (buflen != 0) {
        editorSetStatusMessage("");
        if (callback) callback(buf, c);
        return buf;
      }
    } else if (!iscntrl(c) && c < 128) {
      if (buflen == bufsize - 1) {
        bufsize *= 2;
        buf = realloc(buf, bufsize);
      }
      buf[buflen++] = c;
      buf[buflen] = '\0';
    }

    if (callback) callback(buf, c);
  }
}

void editorMoveCursor(int key) {
  erow *row = (E.cy >= E.numrows) ? NULL : &E.row[E.cy];

  switch (key) {
    case ARROW_LEFT:
      if (E.cx != 0) {
        E.cx--;
      } else if (E.cy > 0) {
        E.cy--;
        E.cx = E.row[E.cy].size;
      }
      break;
    case ARROW_RIGHT:
      if (row && E.cx < row->size) {
        E.cx++;
      } else if (row && E.cx == row->size) {
        E.cy++;
        E.cx = 0;
      }
      break;
    case ARROW_UP:
      if (E.cy != 0) {
        E.cy--;
      }
      break;
    case ARROW_DOWN:
      if (E.cy < E.numrows) {
        E.cy++;
      }
      break;
  }

  row = (E.cy >= E.numrows) ? NULL : &E.row[E.cy];
  int rowlen = row ? row->size : 0;
  if (E.cx > rowlen) {
    E.cx = rowlen;
  }
}

void editorProcessKeypress(const char* serial_port) {
  // 0 -> normal mode
  // 1 -> custom bonus mode
  static char current_mode = 0;
  int c = editorReadKey();

  switch (c) {
    case '\r':
      // if custom bonus mode
      if (current_mode == 1) {
        const char* nl = "\n";
        send_string_to_serial(nl, serial_port, 0);
      } else if (E.cy < E.numrows) {
        send_string_to_serial(E.row[E.cy].chars, serial_port, 1);
      }
      current_mode = 0;
      break;

    case CTRL_KEY('q'):
      write(STDOUT_FILENO, "\x1b[2J", 4);
      write(STDOUT_FILENO, "\x1b[H", 3);
      exit(0);
      break;

    case PAGE_UP:
    case PAGE_DOWN:
      {
        if (c == PAGE_UP) {
          E.cy = E.rowoff;
        } else if (c == PAGE_DOWN) {
          E.cy = E.rowoff + E.screenrows - 1;
          if (E.cy > E.numrows) E.cy = E.numrows;
        }

        int times = E.screenrows;
        while (times--)
          editorMoveCursor(c == PAGE_UP ? ARROW_UP : ARROW_DOWN);
      }
      break;

    case ARROW_UP:
    case ARROW_DOWN:
      editorMoveCursor(c);
      break;

    case ARROW_LEFT:
    case ARROW_RIGHT:
        current_mode = 1;
        char s[2];
        if (c == ARROW_LEFT) {
            c = 216;
        } else {
            c = 215;
        }
        s[0] = c;
        s[1] = 0;
        send_string_to_serial(s, serial_port, 0);
        break;

    default: {
        current_mode = 1;
        char s[2];
        if (c == 27) {
            c = 177;
        }
        s[0] = c;
        s[1] = 0;
        send_string_to_serial(s, serial_port, 0);
      break;
    }
  }
}

void initEditor() {
  E.cx = 0;
  E.cy = 0;
  E.rx = 0;
  E.rowoff = 0;
  E.coloff = 0;
  E.numrows = 0;
  E.row = NULL;
  E.dirty = 0;
  E.filename = NULL;
  E.statusmsg[0] = '\0';
  E.statusmsg_time = 0;
  E.syntax = NULL;

  if (getWindowSize(&E.screenrows, &E.screencols) == -1) die("getWindowSize");
  E.screenrows -= 2;
}

int set_interface_attribs(int fd, int speed, int parity)
{
        struct termios tty;
        if (tcgetattr(fd, &tty) != 0) {
                die("error from tcgetattr");
        }

        cfsetospeed(&tty, speed);
        cfsetispeed(&tty, speed);

        tty.c_cflag = (tty.c_cflag & ~CSIZE) | CS8;     // 8-bit chars
        // disable IGNBRK for mismatched speed tests; otherwise receive break
        // as \000 chars
        tty.c_iflag &= ~IGNBRK;         // disable break processing
        tty.c_lflag = 0;                // no signaling chars, no echo,
                                        // no canonical processing
        tty.c_oflag = 0;                // no remapping, no delays
        tty.c_cc[VMIN]  = 0;            // read doesn't block
        tty.c_cc[VTIME] = 5;            // 0.5 seconds read timeout

        tty.c_iflag &= ~(IXON | IXOFF | IXANY); // shut off xon/xoff ctrl

        tty.c_cflag |= (CLOCAL | CREAD);// ignore modem controls,
                                        // enable reading
        tty.c_cflag &= ~(PARENB | PARODD);      // shut off parity
        tty.c_cflag |= parity;
        tty.c_cflag &= ~CSTOPB;
        tty.c_cflag &= ~CRTSCTS;

        if (tcsetattr(fd, TCSANOW, &tty) != 0) {
                die("error from tcsetattr");
        }
        return 0;
}

void set_blocking(int fd, int should_block)
{
        struct termios tty;
        memset(&tty, 0, sizeof tty);
        if (tcgetattr(fd, &tty) != 0) {
                die("error from tggetattr");
        }

        tty.c_cc[VMIN] = should_block ? 1 : 0;
        tty.c_cc[VTIME] = 5;

        if (tcsetattr (fd, TCSANOW, &tty) != 0) {
                die("error setting serial term attributes");
        }
}

void send_string_to_serial(const char* msg, const char* serial_port, char newline)
{
    if ((msg == NULL) || (strlen(msg) == 0)) {
        return;
    }
    int fd = open(serial_port, O_RDWR | O_NOCTTY | O_SYNC);
    if (fd < 0) {
            die("error opening the serial port");
            return;
    }

    set_interface_attribs(fd, B9600, 0);
    set_blocking (fd, 0);

    for (int i = 0; i < strlen(msg); i++) {
            write(fd, &msg[i], 1);
            usleep(10000);
    }
    if (newline != 0) {
        const char *nl = "\n";
        write(fd, nl, 1);
        usleep(10000);
        close(fd);
    }
}

int test_open_serial(const char* serial_port)
{
    int fd = open(serial_port, O_RDWR | O_NOCTTY | O_SYNC);
    if (fd < 0) {
            printf("error opening the serial port, port: %s, error: %s\n", serial_port, strerror(errno));
            return 1;
    }
    close(fd);
    return 0;
}

void print_usage()
{
    printf("S2S - Send To Serial\n");
    printf("By this program you can view text files and you can send the current line to the serial port\n");
    printf("\n");
    printf("Usage:\n");
    printf("  s2s text_filename serial_port\n");
    printf("\n");
    printf("Example:\n");
    printf("  s2s commands.md /dev/ttyUSB0\n");
    printf("  s2s commands.md /dev/cu.usbserial-A600dXAj\n");
}

int main(int argc, char *argv[]) {
  if (argc != 3) {
    print_usage();
    return 1;
  }

  if (test_open_serial(argv[2]) != 0) {
    return 1;
  }

  enableRawMode();
  initEditor();
  editorOpen(argv[1]);

  editorSetStatusMessage(
    "HELP: Enter = send line | Any key = immediate send | Ctrl-q = quit");

  while (1) {
    editorRefreshScreen();
    editorProcessKeypress(argv[2]);
  }

  return 0;
}
