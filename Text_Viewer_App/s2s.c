// S2S - Send To Serial
// This application a is a special text file viewer program.
// It can show the content of any text file (markdown, bash scripts, source codes, ..)
// The current line can be sent to the serial port by pressing (Enter)
// Additionally most manually typed keystrokes (other than arrows) are sent to the serial port.
// To exit from the program press the CTRL+q
//
// The basis of this application was a simple text editor called "kilo".
// The unnecessary parts such syntax highlighting, editor mode, find, have been removed ...
// Serial port support was added and a feature to be able to send the current
// line to the serial port.
// https://github.com/snaptoken/kilo-src/
//
// This app is intended to be used with an Arduino Teensy or Leonardo running keyboard_automator_emulator.ino
// 
// Compiling the program
// This program does not have any dependency.
// It can be simply compiled by gcc:
// gcc s2s.c -o s2s

